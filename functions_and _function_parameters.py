# functions_and_function_parameters.py

# a regular print, print the item and then prints a \n
print (1)
print (2)
print (3)

# a regular print can print multiple items - each separated by " "
print (1, 2, 3)

l = [6, 3, 2, 4, 1, 5]

# end is "named parameter"
# or "keyword parameter"
for i in l:
    print (i, end=" ")

print () # explicitly print a \n 

# sep is a named or keyword paramter
print (1, 2, 3, 4, sep=",")

print (1, 2, 3, 4, sep="------------------->")

# 1,2,3,4 are "positional parameters"
# sep and end are name or keyword parameters
# named and keyword parameters are a pythonic feature








